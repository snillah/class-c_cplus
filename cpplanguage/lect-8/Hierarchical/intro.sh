What is Hierarchical Inheritance in C++?
Hierarchical inheritance occurs when multiple classes inherit from a single base class.

This allows different derived classes to share common properties or methods from one parent class while also having their own unique features.

Base Class: Shape → provides common behavior (draw())

Derived Classes: Circle, Rectangle, Triangle

Each derived class adds its own specific area calculation.

Demonstrates Hierarchical Inheritance → multiple classes inherit from one parent.