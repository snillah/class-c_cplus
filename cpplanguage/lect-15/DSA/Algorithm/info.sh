What is an Algorithm?
A finite sequence of well-defined instructions to solve a problem.
A finite sequence is a sequence (an ordered list of elements) that has a specific, limited number of terms, 
meaning it ends at a certain point. 
In contrast, an infinite sequence continues indefinitely without an end.

Properties of a Good Algorithm :

Input → Takes some input (may be 0 or more values)

Output → Produces at least one output

Definiteness → Every step is clear and unambiguous

Finiteness → Terminates after a finite number of steps

Effectiveness → Each step can be performed in finite time


 Example:
Linear Search Algorithm in C++
Problem → Find an element in an array

Algorithm Steps
Start from the first element

Compare each element with the target value

If found → return the position

If not found after checking all → return -1

