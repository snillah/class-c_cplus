What is a Linked List?

A Linked List is a linear data structure where elements (called nodes) are connected using pointers.

Each node contains:

Data (value)

Pointer (address of next node)

Unlike arrays:

Memory is not stored contiguously.

Size is dynamic.

Insertion/deletion is faster (no shifting).



2. Types of Linked Lists
Singly Linked List — Each node points to the next node.

Doubly Linked List — Each node points to next and previous node.

Circular Linked List — Last node points back to the first node.


Advantages

Dynamic size — grows/shrinks as needed.
Efficient insertion/deletion (no shifting elements like arrays).

Disadvantages

Extra memory for pointers.
No direct access (must traverse sequentially).
More complex than arrays.

